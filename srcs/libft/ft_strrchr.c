#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *str;

	str = (char *)s;
	while (*str)
		str++;
	while (str >= (char *)s)
	{
		if (*str == (char)c)
			return (str);
		str--;
	}
	return ((void*)0);
}
